Die Menu Position des Custom Post Types ist bei Wordpress nicht wie angenommen frei wählbar 
sondern es gibt genau Vorgaben. Folgende sind vorhanden.
    
5 - Unter Beiträge
10 - Unter Medien
15 - Unter Links
20 - Unter Seiten
25 - Unter Kommentare
60 - Unter first separator
65 - Unter Plugins
70 - Unter Benutzer
75 - Unter Werkzeuge
80 - Unter Einstellungen
100 - Unter second separator
